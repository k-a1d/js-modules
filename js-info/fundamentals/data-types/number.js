// A JS Number represents floating point numbers (numbers with a fractional exponent). 
// There are no separate types for int, long, float, etc. All Numbers are stored as double precision floating point values.

255; // two-hundred and fifty-five
255.0; // same number
255 === 255.0; // true
255 === 0xff; // true (hexadecimal notation)
255 === 0b11111111; // true (binary notation)
255 === 0.255e3; // true (decimal exponential notation)